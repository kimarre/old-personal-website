			<p>At work at the daycare last summer, even though it wasn't in my job description, my favorite part of the day was when I got to watch over the homework room because helping the kids with their reading, math, or cursive homework reaped one of the most rewarding feelings ever.</p>

			<p>In high school, I was the clarinet section leader for two years where I helped to teach my section the music and refine their marching technique. Once I graduated, I wanted to continue helping out with the band program there, so I volunteered to instruct technique and drill formations for two summers of band camp. I'm from Bakersfield, California, which is in the central valley, so even though band camp started in the morning, it was always super hot. By the end of the marching portion at 11am, it would be nearly 100 degrees already. To make matters worse, we didn't even have a grass field available to us like most bands, so our only option was to march on burning hot asphalt and hope that during practice, we wouldn't spontaneously burst into flames. It was like marching on the surface of the sun. Regardless of the circumstances, every day I was willing to risk melting into the hot asphalt to help the kids better their marching skills. Watching them improve over time and knowing that I had contributed to that made the risk of melting so worth it.</p> 

